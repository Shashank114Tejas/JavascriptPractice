/**The reverse() method reverses an array in place and returns the reference to the same
 *  array, the first array element now becoming the last, and the last array element 
 * becoming the first. In other words,
 *  elements order in the array will be turned towards the direction opposite 
 * to that previously stated. */

const arr=[1,2,3,4,5,6,7,8]
console.log(arr.reverse());//[8, 7, 6, 5, 4, 3, 2, 1]
console.log(arr);//[8, 7, 6, 5, 4, 3, 2, 1]
